class Solution {
public:
    int chalkReplacer(std::vector<int>& chalk, int k) {
        int numStudents = chalk.size();
        long long cycleChalk = 0;

        for (auto& num : chalk) {
            cycleChalk += num;
        }

        int remainingChalk = k % cycleChalk;

        while (remainingChalk >= 0) {
            if (remainingChalk - cycleChalk > 0) {
                remainingChalk -= cycleChalk;
            } else {
                for (int idx = 0; idx < numStudents; ++idx) {
                    remainingChalk -= chalk[idx];

                    if (remainingChalk < 0) return idx;
                }    
            }
        }

        return -1;
    }
};

